



a = int(input("Digite um N° : "))
b = int(input("Digite um N° : "))
soma = a + b
print(f"A soma entre a e b = {soma}")

c = int(input("Digite um N° : "))

if soma > c:
    print(f" A soma de a + b é maior que c : {soma}")

elif soma == c:
    print(f"N° iguais {soma} = {c}")

else:
    print(f"C é maior que a soma de a + b : {c}")

