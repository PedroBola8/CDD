numero = float(input("Digite o 1° N°: "))
numero02 = float(input("Digite o 2° N°: "))

soma = ((numero + numero02))
subtracao = ((numero - numero02))
multi = ((numero * numero02))
divi = ((numero / numero02))

print(f"Os valores obtidos das operações foram:"
      f"\n soma: {soma} "
      f"\n subtração: {subtracao} "
      f"\n Multiplicação: {multi} "
      f"\n divi: {divi}"  )