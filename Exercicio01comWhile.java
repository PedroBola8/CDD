package Casa;

public class Exercicio01comWhile {
	public static void main(String[] args) {
		int idade = 19;
		while (idade <= 25) {
			System.out.println(idade);
			idade = idade +1;
		}
			
		while (idade >= 13) {
			System.out.println(idade);
			idade = idade-1;
		}
		
	}

	
}
