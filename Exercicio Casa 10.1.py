n1 = 500
'''crei uma variavel para armazenar o valor pedido'''
'''while é um enquanto, ou seja, enquanto a variavel n1 for maior ou igual a 1 ela vai se repetir'''
while n1 >= 1:
    if n1 %2 !=0 :
        print(n1, end = " ")
        ''' o end= " " é só para organizar em uma linha'''
    n1-=1
    '''variavel seguida de sinal negativo é para colocar na forma decrescente'''